package svsminiproject;

public class Place {
	 private int id;
	    private String name;

	    public Place(int id, String name) {
	        this.id = id;
	        this.name = name;
	    }

	    public int getId() {
	        return id;
	    }

	    public String getName() {
	        return name;
	    }
}
